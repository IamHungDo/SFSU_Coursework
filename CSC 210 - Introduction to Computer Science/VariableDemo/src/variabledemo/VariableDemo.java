public class VariableDemo {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int number1;
        number1 = 10;

        int number2 = -2;

        int sum;
        sum = number1 + number2;

        number2 = 0;
        System.out.println(sum);

        sum = number1 + number2;
        System.out.println(sum);

    }
    int number1;
}
